    private void ConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmarActionPerformed
        try {
            new DAOContaBar().realizarVenda(associado, calcTotal());
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Driver n√£o encontrado.");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro na conex√£o com o banco ou na consulta.");
        }
            JOptionPane.showMessageDialog(null, "Registro inserido com sucesso.");
            setVisible(false);
    }//GEN-LAST:event_ConfirmarActionPerformed
    private double calcAlmoco() throws ClassNotFoundException, SQLException{
        int quantAlmoco = (int) jsAlmoco.getValue();
        Produto almoco = null;
        almoco = new DAOProduto().buscarPorTipo("ALMOCO");
        double valorAlmoco = almoco.getValor() * quantAlmoco;
        return valorAlmoco;
    }
    
    private double calcLanche() throws ClassNotFoundException, SQLException{
        int quantLanche = (int) jsLanche.getValue();
        Produto lanche = null;
        lanche = new DAOProduto().buscarPorTipo("LANCHE");
        double valorAlmoco = lanche.getValor() * quantLanche;
        return valorAlmoco;
    }
    
    private double calcTotal() throws ClassNotFoundException, SQLException{
        return calcAlmoco()+calcLanche();
    }
    
    
